int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb > 0)
		while (i <= nb / 2)
		{
			if (nb == i * i)
				return (i);
			else
				i++;
		}
	if (i > nb / 2)
		return (0);
	return (0);
}
