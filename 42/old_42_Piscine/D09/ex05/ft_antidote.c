int		ft_antidote(int i, int j, int k)
{
	if ((i > j && i < k) || (i > k && i < j))
		return (i);
	if ((k > i && k < j) || (k > j && k < j))
		return (k);
	if ((j > k && j < i) || (j > i && j < k))
		return (j);
	else
		return (0);
}
