int	ft_atoi(char *str)
{
	int neg;
	int value;

	neg = 1;
	value = 0;
	while ((*str >= 9 && *str <= 13) || (*str == 32))
		str++;
	if (*str == '-')
		neg = -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		value *= 10;
		value += *str - 48;
		str++;
	}
	return (value * neg);
}
