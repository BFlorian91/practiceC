#include <stdio.h>
//Create a Squarre with value static;

int		main()
{
	int		x;
	int		y;
	
	x = 0;
	y = 0;
	while (x <= 8)
	{
		printf("+");
		if ((x == 2 && y <= 1) || (x == 5 && y <= 2) || (x == 8 && y <= 3))
		{
			printf("\n");
			y++;
		}
		x++;
	}
	return(0);
}

